package com.lael.infra.modules.member;

public class Member {

	private String memSeq;
	private Integer memAge;
	private Integer memGender;
	private String memName;
	private String memFiled;
	
	
	public String getMemSeq() {
		return memSeq;
	}
	public void setMemSeq(String memSeq) {
		this.memSeq = memSeq;
	}
	public Integer getMemAge() {
		return memAge;
	}
	public void setMemAge(Integer memAge) {
		this.memAge = memAge;
	}
	public Integer getMemGender() {
		return memGender;
	}
	public void setMemGender(Integer memGender) {
		this.memGender = memGender;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	public String getMemFiled() {
		return memFiled;
	}
	public void setMemFiled(String memFiled) {
		this.memFiled = memFiled;
	}
	
	
	
}
